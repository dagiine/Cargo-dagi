// Backend-ээс ирсэн category-г нэг ижил хэлбэртэй object болгоно.
export function normalizeCategory(item) {
  if (typeof item === "string") {
    return { name: item, icon: "help" };
  }

  return {
    name: item.name || item.category || "Ерөнхий",
    icon: item.icon || "help",
  };
}

// Backend-ээс ирсэн FAQ-г frontend-д ашиглахад амар хэлбэрт оруулна.
export function normalizeFaq(item) {
  return {
    question: item.question || item.title || "Асуулт",
    answer: item.answer || item.description || "",
    category: item.category || "Захиалга",
  };
}

// Давхардсан category-г нэгтгэнэ.
// Map ашигласнаар category нэр давхардвал хамгийн сүүлчийн утгаар шинэчлэгдэнэ.
export function mergeCategories(categories, faqs) {
  const categoryMap = new Map();

  [...DEFAULT_CATEGORIES, ...categories].forEach((item) => {
    const category = normalizeCategory(item);
    categoryMap.set(category.name, category);
  });

  faqs.forEach((faq) => {
    if (!categoryMap.has(faq.category)) {
      categoryMap.set(faq.category, { name: faq.category, icon: "help" });
    }
  });

  return Array.from(categoryMap.values());
}
